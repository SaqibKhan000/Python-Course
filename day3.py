
#################### DATA TYPES IN PYTHON ########################

# 1) NUMERIC DATA TYPES:
# a) Integer
num1 = 10
print(type(num1))

# b) Float
num2 = 2.43
print(type(num2))

# c) Complex
num3 = complex(2, 8) # 2 + 8i
print(type(num3))



# 2) SEQUENCE DATA TYPES:
# a) String
myName = "M Saqib Khan"
print(type(myName))

# b) List
numbersAndLetters = [1, 2, 'a', 'b']
print(type(numbersAndLetters))

# c) Tupple
numbers = (1, 2, 3, 4)
print(type(numbers))



# 3) Dictionary DATA TYPE:
userData = {
    'name': 'M Saqib Khan',
     'address': 'Peshawar'
}
print(type(userData))



# 4) SET DATA TYPE:
randomData = { 20, 'M Saqib Khan', True, 5.5, 'Data' }
print(type(randomData))



# 5) BOOLEAN DATA TYPES:
bool = True # or False
print(type(bool))



# 6) BINARY DATA TYPES:
# a) Bytes
name = b'Ali'
print(type(name))

# b) Bytearray
# c) Memoryview